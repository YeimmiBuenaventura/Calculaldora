//Se declara una clase Calculadora
class Calculadora {
    //Declarando Metodos que va a ejecutar la clase
     sumar(num1, num2) {
        return num1 + num2; //Metodo Sumar
    }

    restar(num1, num2) {
        return num1 - num2; //Metodo Restar
    }

    dividir(num1, num2) {
        return num1 / num2; //Metodo Dividir
    }

    multiplicar(num1, num2) {       
        return num1 * num2;  //Metodo Multiplicar
    }
} 